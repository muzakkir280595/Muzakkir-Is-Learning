
public class doWhileLoop {
	
	//Different between while loop and do while loop is, while loop will start checking of while at the start of the progress, for do while, the checking start after do method.

	public static void main(String[] args) {
		
		int counter=0;
		
		do {
			System.out.println(counter);
			counter++;
		}while(counter<=10);
		

	}

}

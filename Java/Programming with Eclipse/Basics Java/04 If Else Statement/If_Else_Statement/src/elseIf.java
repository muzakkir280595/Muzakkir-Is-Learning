
public class elseIf {

	public static void main(String[] args) {
		int age = 45;
		
		if (age >= 60) {
			System.out.println("You Are Senior Citizen.HOOORAYYYY!!!!!!");
		}
		
		else if (age >= 50) {
			System.out.println("You Are 50???BOOOO");
		}
		
		else if (age >= 40) {
			System.out.println("You Are 40???BOOOO");
		}
		
		else {
			System.out.println("You Are Young Buck");
		}

	}

}
